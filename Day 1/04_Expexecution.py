#String and numeric values can operate to  *
A = 2
B = 3
txt = "@"
print(2*txt*3)

#String and string can be operated with +
A = "2"
B = 3
txt = '@'
print ((A + txt) * B)

#Numeric values  can operate with all arithmetic operators
A = 2
B = 3
C = 4
print(A + B * C)

#Arithmetic expression with integer and float will result float
A = 2.5
B = 4
print(A * B + A / B )

#Result of division is always float
A = 5
B = 2
print(A / B)

#Integer division with float and int will give int displayed as flat
