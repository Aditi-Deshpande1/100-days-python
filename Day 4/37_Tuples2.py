tup = (1,) # for single element tuple we have to add comma after the element 
print(tup)
print(type(tup))

tup = (1) # this is not a tuple
print(tup)
print(type(tup))

# if we don't use parenthesis it will also be considered as tuple but it's data type will be integer

tup = (1.5) # this is not a tuple
print(tup)
print(type(tup))

tup = ("Aditi") # this is not a tuple
print(tup)
print(type(tup))
